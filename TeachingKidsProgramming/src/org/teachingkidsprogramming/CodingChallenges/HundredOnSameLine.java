package org.teachingkidsprogramming.CodingChallenges;

public class HundredOnSameLine
{
  public static void main(String[] args)
  {
    for (int i = 0; i < 100; i++)
    {
      System.out.print(i);
    }
    System.out.println();
    System.out.println();
    for (int i = 1; i < 101; i++)
    {
      System.out.print(i);
    }
  }
}
